    public static void seePath(String name) {
        // Dosya yolu ve dosya adı
        String working = System.getProperty("user.dir");
        String dosyaYolu =working+File.separator+"Projects"+File.separator+projectName+File.separator;
        String dosyaAdi = "deneme.c";

        // Dosya okuma ve yazma işlemleri
        try {
            // Okunacak dosyanın yolu
            String okunacakDosyaYolu = dosyaYolu + dosyaAdi;
            
            // Okunan dosyadan veri oku
            BufferedReader okuyucu = new BufferedReader(new FileReader(okunacakDosyaYolu));
            
            // Yazılacak dosyanın adı ve yolu
            String ciktiDosyaYolu = "C:\\path\\to\\cikti.txt";

            // Çıktı dosyasına veri yaz
            BufferedWriter yazici = new BufferedWriter(new FileWriter(ciktiDosyaYolu));
            String satir;
            while ((satir = okuyucu.readLine()) != null) {
                yazici.write(satir);
                yazici.newLine(); // Yeni satır ekle
            }

            // Dosyaları kapat
            okuyucu.close();
            yazici.close();

            System.out.println(dosyaAdi + " dosyasının içeriği başarıyla " + ciktiDosyaYolu + " dosyasına yazıldı.");
        } catch (IOException e) {
            System.out.println("Dosya işlemleri sırasında bir hata oluştu: " + e.getMessage());
        }
    }

