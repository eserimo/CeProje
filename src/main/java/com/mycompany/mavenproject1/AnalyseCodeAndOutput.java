    public static ArrayList<String> dosyaIceriginiOku(String dosyaYolu) {
        ArrayList<String> satirlar = new ArrayList<>();

        try {
            File dosya = new File(dosyaYolu);
            BufferedReader okuyucu = new BufferedReader(new FileReader(dosya));

            String satir;
            while ((satir = okuyucu.readLine()) != null) {
                satirlar.add(satir);
            }

            okuyucu.close();
        } catch (IOException e) {
            System.out.println("Dosya okuma hatası: " + e.getMessage());
        }

        return satirlar;
    }
